def _get_fallback_data() -> tuple[list[dict], list[dict], list[dict]]:
    """生成内置样例数据集（train/val/test = 60%/20%/20%）。"""
    random.seed(42)
    neg = [{"text": t, "label": 0} for t in _FALLBACK_NEG]
    pos = [{"text": t, "label": 1} for t in _FALLBACK_POS]
    all_data = neg + pos
    random.shuffle(all_data)

    n = len(all_data)
    train_end = int(n * 0.6)
    val_end = int(n * 0.8)
    return all_data[:train_end], all_data[train_end:val_end], all_data[val_end:]


def load_dataset(split: str = "train") -> list[dict]:
    """
    加载文本分类数据，返回 [{"text": ..., "label": ...}, ...]。

    split: "train" | "validation" | "test"
    优先从 HuggingFace 下载，失败则使用内置数据。
    """
    try:
        from datasets import load_dataset as hf_load

        ds = hf_load("shibing624/chnsenticorp", split=split, trust_remote_code=True)
        records = [{"text": row["text"], "label": int(row["label"])} for row in ds]
        print(f"  [HuggingFace] 已加载 {split} 集：{len(records)} 条")
        return records
    except Exception:
        print(f"  [Fallback] HuggingFace 加载失败，使用内置样例数据")
        fallback = _get_fallback_data()
        idx = {"train": 0, "validation": 1, "test": 2}[split]
        records = fallback[idx]
        print(f"  [Fallback] {split} 集：{len(records)} 条")
        return records


def get_label_names() -> dict[int, str]:
    """返回标签映射：{0: "负面", 1: "正面"}。"""
    return {0: "负面", 1: "正面"}


